export type DictionaryKey = keyof typeof dictionaries.en;

export function pickLocalized<T extends object>(
  object: T,
  field: string,
  locale: Locale
) {
  const source = object as Record<string, unknown>;
  const localized = source[`${field}_${locale}`];
  const fallback = source[`${field}_en`];
  return String(localized ?? fallback ?? "");
}
